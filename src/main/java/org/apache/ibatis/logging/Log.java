package org.apache.ibatis.logging;

/**
 *  日志接口,mybatis为了不和某个特定日志框架耦合，提供了自己的日志接口，然后通过适配器模式灵活的选择用户提供的日志框架
 */
public interface Log {

  boolean isDebugEnabled();

  boolean isTraceEnabled();

  void error(String s, Throwable e);

  void error(String s);

  void debug(String s);

  void trace(String s);

  void warn(String s);

}
